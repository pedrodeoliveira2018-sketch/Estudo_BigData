#Faça um algoritmo, que leia um número em metros e
#  converta-o para centímetros e milímetros.

metros = int(input('me dia a quantos metros voce ets ?: ' ))
centímetros = metros * 100
milímetros = metros  * 1000
print(f'ok : {metros}')
print(f'então voce esta a  {centímetros}  centimetro de casa ')
print(f'ou a  {milímetros} milimetros de casa ')